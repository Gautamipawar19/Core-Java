class Concat1
{
	public static void main(String args[])
	{
		byte a=10;
		System.out.println("the value of a is " +a);
		short b=20;
		System.out.println("the value of b is " +b);
		int c=30;
		System.out.println("the value of c is " +c);
		long d=40;
		System.out.println("the value of d is " +d);
	
	}

}
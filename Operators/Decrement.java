class Decrement{
  public static void main(String args[])
  {
	int i=1;
	System.out.println(i);
	//predecrement-> first decrement the  i value + perform the operation(print)
	
	System.out.println(--i);
	
	System.out.println(i);
	//postdecrement->perform the operation(print) + first decrement the  i value
	
	System.out.println(i--);
	
	System.out.println(i);
  }

}
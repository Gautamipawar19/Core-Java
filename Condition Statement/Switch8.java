class Switch8
{
	public static void main(String args[])
 {
	System.out.println("Start @ cyber success");
	String s="cyber";
	switch(s)
	{
	 case "cyber": System.out.println("Inside cyber");
		break;
		
	 case "success": System.out.println("Inside success");
		break;
		
	 default: System.out.println("Inside default case");
		break;
			
	}
	System.out.println("Stop @ cyber success");
 
 }
 }
 //one to five onwards string is also allowed in switch statement.